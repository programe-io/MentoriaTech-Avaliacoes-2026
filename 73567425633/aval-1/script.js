// VARIÁVEIS

// var (forma antiga - evite usar)
var nome = "João";

// let (pode mudar o valor)
let idade = 25;

// const (valor fixo, não pode ser reatribuído)
const cidade = "São Paulo";


// TIPOS DE DADOS

// String (texto)
let texto = "Olá, mundo!";

// Number (número)
let numero = 10;

// Boolean (verdadeiro ou falso)
let ativo = true;

// Null (valor vazio intencional)
let vazio = null;

// Undefined (não definido)
let indefinido;

// Array (lista)
let frutas = ["maçã", "banana", "laranja"];

// Object (objeto)
let pessoa = {
  nome: "Maria",
  idade: 30,
  cidade: "Rio de Janeiro"
};


// EXIBINDO NO CONSOLE
console.log(nome);
console.log(idade);
console.log(cidade);

console.log(texto);
console.log(numero);
console.log(ativo);
console.log(vazio);
console.log(indefinido);
console.log(frutas);
console.log(pessoa);